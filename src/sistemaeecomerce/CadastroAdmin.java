    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String n;
        int i;
        String e;
        String l;
        String s;
        n = nome.getText();
        try {
            i = Integer.parseInt(idade.getText());
        } catch(Exception e1) {
            i = 0;
        }
        e = email.getText();
        l = login.getText();
        s = senha.getText();
        
        Admin p = new Admin();
        p.setLogin(l);
        p.setSenha(s);
        p.setEmail(e);
        p.setNome(n);
        p.setIdade(i);
        
        p.inserir(this.cep.getText(), this.rua.getText(), this.complemento.getText(), (String) this.estado.getSelectedItem(), this.cidade.getText());
        p.inserirAdmin();
        
        System.out.println("Cadastro concluido!");
        System.out.println("Nome: " + n);
        System.out.println("Idade: " + i);
        System.out.println("Email: " + e);
        System.out.println("Login: " + l);
        System.out.println("Senha: " + s);
        JOptionPane.showMessageDialog(null, "Cadastro Conclu√≠do!");
    }//GEN-LAST:event_jButton1ActionPerformed

